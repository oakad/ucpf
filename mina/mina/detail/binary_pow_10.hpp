	template <typename T>
	static entry<T> lookup_pow_10(int32_t pow_2)
	{
		constexpr static int bits = sizeof(T) * 8;

		double k(std::ceil(inv_log2_10 * (pow_2 + bits - 1)));
		auto idx(std::lround(k));
		idx -= pow_10_range.first + 1;
		idx /= pow_10_step;
		auto pow_10((idx + 1) * pow_10_step + pow_10_range.first);
		return entry<T>(
			pow_10_list[idx + 1], pow_10,
			std::lround(log2_10 * pow_10 + 0.5)
		);
	}

